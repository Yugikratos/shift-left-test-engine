CREATE MULTISET TABLE fin_ctl.etl_cyc_ctl ,FALLBACK ,
     NO BEFORE JOURNAL,
     NO AFTER JOURNAL,
     CHECKSUM = DEFAULT
     (
      ETL_CYC_DT DATE FORMAT 'YYYY-MM-DD' NOT NULL,
      ETL_PROC_CD CHAR(10) CHARACTER SET LATIN NOT CASESPECIFIC NOT NULL,
      ETL_CYC_STRT_TS TIMESTAMP(6),
      ETL_CYC_END_TS TIMESTAMP(6),
      ETL_CYC_FREQ_CD VARCHAR(20) CHARACTER SET LATIN NOT CASESPECIFIC,
      ETL_CYC_STS_CD VARCHAR(20) CHARACTER SET LATIN NOT CASESPECIFIC,
      ETL_CYC_ID BIGINT)
UNIQUE PRIMARY INDEX ETL_CYC_CTL_PK ( ETL_CYC_DT ,ETL_PROC_CD );

